package ksnu.a1901070.score;

public class Subject {
	int ko;
	int en;
	int ma;
	
	public Subject() {
		this.ko=0;
		this.en=0;
		this.ma=0;
	}
	
	
	public int getKo() {
		return ko;
	}
	public void setKo(int ko) {
		this.ko = ko;
	}
	public int getEn() {
		return en;
	}
	public void setEn(int en) {
		this.en = en;
	}
	public int getMa() {
		return ma;
	}
	public void setMa(int ma) {
		this.ma = ma;
	}
	
	
}
